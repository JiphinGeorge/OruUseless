// Function to translate text to emojis
document.getElementById("translate-button").addEventListener("click", function() {
    const inputText = document.getElementById("input-text").value.toLowerCase();
    let translatedText = inputText;

    // Replace words with emojis
    for (const [word, emoji] of Object.entries(emojiMap)) {
        const regex = new RegExp(`\\b${word}\\b`, "g");
        translatedText = translatedText.replace(regex, emoji);
    }

    document.getElementById("emoji-display").innerText = translatedText || "Please enter some text!";
});

// Clear input and output
document.getElementById("clear-button").addEventListener("click", function() {
    document.getElementById("input-text").value = '';
    document.getElementById("emoji-display").innerText = '';
});
